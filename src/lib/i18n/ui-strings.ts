// ‚îÄ‚îÄ‚îÄ Helper to resolve language ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

/** Map browser locale or detectLanguage() output to a UILang */
export function resolveUILang(input?: string | null): UILang {
    if (!input) return 'English';
    const lower = input.toLowerCase().trim();

    // Direct match from detectLanguage() output
    const directMap: Record<string, UILang> = {
        english: 'English', portuguese: 'Portuguese', spanish: 'Spanish',
        french: 'French', german: 'German', italian: 'Italian', dutch: 'Dutch',
    };
    if (directMap[lower]) return directMap[lower];

    // BCP-47 locale prefix match (e.g. "pt-BR", "de-DE", "fr")
    const prefix = lower.split('-')[0];
    const localeMap: Record<string, UILang> = {
        en: 'English', pt: 'Portuguese', es: 'Spanish',
        fr: 'French', de: 'German', it: 'Italian', nl: 'Dutch',
    };
    return localeMap[prefix] || 'English';
}

/** Get a translated string. Falls back to English if key missing. */
export function t(lang: UILang, key: string): string {
    return strings[lang]?.[key] || strings.English[key] || key;
}

/** Get the full phase labels map for a given language */
export function getPhaseLabels(lang: UILang): Record<string, string> {
    return {
        round1_analysis: t(lang, 'dc_phase_round1'),
        round2_attack: t(lang, 'dc_phase_round2'),
        round3_defense: t(lang, 'dc_phase_round3'),
        final_verdict: t(lang, 'dc_phase_verdict'),
    };
}
